export function personalizeNews(templates, profile) {
  const name = profile.name || 'Jogador'
  const team = profile.favoriteTeam || 'a equipa'
  const player = profile.favoritePlayer || 'o jogador'

  return templates.map((t) => ({
    ...t,
    headline: t.headline.replace(/\{name\}/g, name).replace(/\{team\}/g, team).replace(/\{player\}/g, player),
    body: t.body.replace(/\{name\}/g, name).replace(/\{team\}/g, team).replace(/\{player\}/g, player),
    fact: t.fact.replace(/\{name\}/g, name).replace(/\{team\}/g, team).replace(/\{player\}/g, player),
    question: t.question.replace(/\{name\}/g, name).replace(/\{team\}/g, team).replace(/\{player\}/g, player),
  }))
}
